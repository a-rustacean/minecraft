pub const Brightness = enum(u2) {
    Low = 0,
    Normal = 1,
    High = 2,
    Lowest = 3,

    pub fn getModifier(brightness: Brightness) u8 {
        return switch (brightness) {
            .Low => 180,
            .Normal => 220,
            .High => 255,
            .Lowest => 135,
        };
    }
};
